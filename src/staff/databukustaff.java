    private void bpinjamActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bpinjamActionPerformed
         // Cek apakah baris sudah diklik
    if (kdbuku == null || jdlbuku == null || kategori == null) {
        JOptionPane.showMessageDialog(null, "Pilih buku terlebih dahulu!");
        return;
    }

    // Kirim data buku ke formpeminjamanstaff
    formpeminjamanstaff f = new formpeminjamanstaff();
    f.kdbuku = this.kdbuku;
    f.jdlbuku = this.jdlbuku;
    f.kategori = this.kategori;
    f.itemTerpilih(); // set data di form peminjaman

    f.setVisible(true);
    this.dispose(); 
    }//GEN-LAST:event_bpinjamActionPerformed

