     public void cetak() {
      try{
           String path="./src/laporan/lapbuku.jasper";
           HashMap parameter = new HashMap();
           JasperPrint print = JasperFillManager.fillReport(path,parameter,conn);
           JasperViewer.viewReport(print,false);
       }catch(Exception ex){
           JOptionPane.showMessageDialog(rootPane,"Dokumen Tidak Ada"+ex);
       }
}
     protected void datatable(){
      Object[] Baris = {"Kode Buku", "Judul", "Kategori", "Pengarang", "Penerbit", "Stok", "Tahun", "Halaman", "Deskripsi"};
    tabmode = new DefaultTableModel(null, Baris);
    String cariitem = txtcaribukustaff.getText();

    try {
        String sql = "SELECT * FROM buku WHERE kode_buku LIKE '%" + cariitem + "%' OR judul_buku LIKE '%" + cariitem + "%' ORDER BY kode_buku";
        Statement stat = conn.createStatement();
        ResultSet hasil = stat.executeQuery(sql);
        while (hasil.next()) {
            tabmode.addRow(new Object[]{
                hasil.getString("kode_buku"),
                hasil.getString("judul_buku"),
                hasil.getString("kategori"),
                hasil.getString("pengarang"),
                hasil.getString("penerbit"),
                hasil.getInt("stok"),
                hasil.getString("tahun_terbit"),
                hasil.getInt("halaman"),
                hasil.getString("deskripsi")
            });
        }
        tblbukustaff.setModel(tabmode);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Data gagal dipanggil: " + e);
    }
     }
