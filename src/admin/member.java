    public void cetak() {
     int selectedRow = tblmember.getSelectedRow();
if (selectedRow == -1) {
    JOptionPane.showMessageDialog(this, "Pilih data member terlebih dahulu.");
    return;
}

String id = tblmember.getValueAt(selectedRow, 0).toString(); // ambil ID dari tabel

try {
    String path = "./src/laporan/kartu.jasper";
    HashMap<String, Object> parameter = new HashMap<>();
    parameter.put("id", id); // HARUS 'id', sesuai dengan $P{id} di laporan

    JasperPrint print = JasperFillManager.fillReport(path, parameter, conn);
    JasperViewer.viewReport(print, false);
} catch (Exception ex) {
    JOptionPane.showMessageDialog(rootPane, "Dokumen Tidak Ada: " + ex);
}
}
    protected void datatable(){
      Object[] Baris = {"ID Member", "Nama ", "NISN", "Jurusan", "Alamat", "No Telepon", "Tanggal Daftar", "Status"};
    tabmode = new DefaultTableModel(null, Baris);
    String cariitem = txtcari.getText();

    try {
        String sql = "SELECT * FROM member WHERE id_member LIKE '%" + cariitem + "%' OR nama LIKE '%" + cariitem + "%' ORDER BY id_member";
        Statement stat = conn.createStatement();
        ResultSet hasil = stat.executeQuery(sql);
        while (hasil.next()) {
            tabmode.addRow(new Object[]{
                hasil.getString("id_member"),
                hasil.getString("nama"),
                hasil.getString("nisn"),
                hasil.getString("jurusan"),
                hasil.getString("alamat"),
                hasil.getString("no_telepon"),
                hasil.getString("tanggal_daftar"),
                hasil.getString("status")
            });
        }
        tblmember.setModel(tabmode);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Data gagal dipanggil: " + e);
    }
     }
