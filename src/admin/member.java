    private void bsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsimpanActionPerformed
       String sql = "INSERT INTO member VALUES (?, ?, ?, ?, ?, ?, ?, ?)";
    try {
        PreparedStatement stat = conn.prepareStatement(sql);
        stat.setString(1, txtidmember.getText());
        stat.setString(2, txtnmmember.getText());
        stat.setString(3, txtnisn.getText());
        stat.setString(4, txtjurusan.getText());
        stat.setString(5, txtalmt.getText());
        stat.setString(6, txttlp.getText());  
        java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("yyyy-MM-dd");
        String tahun = sdf.format(jdctgl.getDate());
        stat.setString(7, tahun);   
        stat.setString(8, cbstatus.getSelectedItem().toString());
        stat.executeUpdate();
        JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
        kosong();
        txtidmember.requestFocus();
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Data gagal disimpan: " + e);
    }
    datatable(); 
    }//GEN-LAST:event_bsimpanActionPerformed

