    private void bsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsimpanActionPerformed
       String sql = "INSERT INTO pengembalian VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?,?)";
    try {
        PreparedStatement stat = conn.prepareStatement(sql);
        stat.setString(1, txtidkembali.getText());
        stat.setString(2, txtidstaff.getText());
        stat.setString(3, txtidpinjam.getText());        
        stat.setString(4, txtidmbr.getText());
        stat.setString(5, txtkdbuku.getText());
        stat.setString(6, txtjdlbk.getText());

        java.text.SimpleDateFormat sdf1 = new java.text.SimpleDateFormat("yyyy-MM-dd");
        String tanggalPengembalian = sdf1.format(jdcpengembalian.getDate());
        stat.setString(7, tanggalPengembalian);

        java.text.SimpleDateFormat sdf2 = new java.text.SimpleDateFormat("yyyy-MM-dd");
        String tanggalKembali = sdf2.format(jdckembali.getDate());
        stat.setString(8, tanggalKembali);
        
        stat.setString(9, txtterlambat.getText());
                
        stat.setString(10, cbstatus.getSelectedItem().toString());

        stat.executeUpdate();
        JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
        kosong();
        txtidkembali.requestFocus();
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Data gagal disimpan: " + e);
    }
    
    
    datatable(); 
    }//GEN-LAST:event_bsimpanActionPerformed

